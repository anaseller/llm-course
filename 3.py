def count_vowels(text):
    """
    Подсчитывает количество гласных букв в строке.
    
    Аргументы:
    text (str): Входная строка для анализа
    
    Возвращает:
    int: Количество гласных букв в строке
    
    Пример:
    >>> count_vowels("Привет, мир!")
    3
    """
    # Создаем множество гласных букв (как строчных, так и заглавных)
    vowels = set('аеёиоуыэюяАЕЁИОУЫЭЮЯ')
    # Считаем количество гласных в строке
    return sum(1 for char in text if char in vowels)

# Пример использования
if __name__ == "__main__":
    test_string = "Программирование - это интересно!"
    result = count_vowels(test_string)
    print(f"В строке '{test_string}' {result} гласных букв")
